int TTimeZone::GetDaylightTimeStartDate(int nDst, int nYear, VCTIME &vcStart)
{
	int a[8];
	nDst = ID2INDEX(nDst);
	ExpandVal(gzone[nDst].val, a);
	vcStart.day = 1;
	vcStart.month = a[0];
	vcStart.year = nYear;
	if (a[1] == 1)
	{
		vcStart.day = a[2];
	}
	else
	{
		if (a[2] == 5)
		{
			vcStart.day = GetMonthMaxDays(nYear, a[0]);
			vcStart.InitWeekDay();
			while (vcStart.dayOfWeek != a[3])
			{
				vcStart.PreviousDay();
				vcStart.dayOfWeek = (vcStart.dayOfWeek + 6) % 7;
			}
		}
		else
		{
			vcStart.day = 1;
			vcStart.InitWeekDay();
			while(vcStart.dayOfWeek != a[3])
			{
				vcStart.NextDay();
				vcStart.dayOfWeek = (vcStart.dayOfWeek + 1) % 7;
			}
			vcStart.day += a[2] * 7;
		}
	}
	vcStart.shour = 1/24.0;
	return 0;
}

int TTimeZone::GetNormalTimeStartDate(int nDst, int nYear, VCTIME &vcStart)
{
	vcStart.day = 1;
	vcStart.month = 10;
	vcStart.year = nYear;
	vcStart.shour = 3/24.0;
	return 0;
}


int TTimeZone::GetID(const char *p)
{
	int i = 0;
	int m = GetTimeZoneCount();
	for(i = 0; i < m; i++)
	{
		if (stricmp(p,TTimeZone::gzone[i].name) == 0)
			return TTimeZone::gzone[i].idx;
	}
	for(i = 0; i < m; i++)
	{
		if (strstr(TTimeZone::gzone[i].name, p) != 0)
			return TTimeZone::gzone[i].idx;
	}
	return atoi(p);
}

int TTimeZone::ID2INDEX(int _id)
{
	int i, m;
	for(i = 0, m = GetTimeZoneCount(); i < m; i++)
	{
		if (_id == gzone[i].idx)
			return i;
	}

	return 0;
}

int TTimeZone::NAME2INDEX(const char * aName)
{
	int i, m;
	for(i = 0, m = GetTimeZoneCount(); i < m; i++)
	{
		if (strcmp(gzone[i].name,aName) == 0)
			return i;
	}

	return 0;
}

