void TTimeZone::ExpandVal(unsigned int val, int a[])
{
	a[7] = val & 0xf;
	val >>= 4;
	a[6] = val & 0x3f;
	val >>= 6;
	a[5] = val & 0x3;
	val >>= 2;
	a[4] = val & 0xf;
	val >>= 4;
	a[3] = val & 0xf;
	val >>= 4;
	a[2] = val & 0x3f;
	val >>= 6;
	a[1] = val & 0x3;
	val >>= 2;
	a[0] = val & 0xf;	

}

